public class GallonsToLiters {
    public static void main(String[] args) {
        byte gallon = 10;
        double litterPerGallon = 4.54609;
        double result = gallon/litterPerGallon;

        System.out.println("result = " + result);

        System.out.println(gallon + " gallons equal to " + result + " liters. ");



    }


}
