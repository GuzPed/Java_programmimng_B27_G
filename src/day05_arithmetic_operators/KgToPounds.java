package day05_arithmetic_operators;

public class KgToPounds {
    public static void main(String[] args) {
        int kg = 125;
        double poundInKg = 2.20462;
        double result = kg / poundInKg;

        System.out.println( kg + " kg equal to " + result + " pounds");


        int kg1 = 17;
        float pound = 2.20462F;
        float result2 = kg1 / pound;

        System.out.println(kg1 + " kg equal to " + result2 + " pounds");



    }


}
